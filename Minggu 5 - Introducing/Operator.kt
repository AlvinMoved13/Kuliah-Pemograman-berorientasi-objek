fun main() {  
  var a = 5 //pengurangan
  a -= 3
  println(a)  
  
    
  var b = 5 //penambahan
  b += 3
  println(b)  
  
    
  var c = 5 //perkalian
  c *= 3
  println(c)  
  
    
  var d = 6 //pembagian
  d /= 3
  println(d)  
  
    
  var e = 5 //boolean
  e %= 3
  println(e)  
}